    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {

        $plant_reference = Plant_referencepage::with('products', 'products.retailer')->find($id);
        $storeIdList = [];
        foreach ($plant_reference->products as $product) {
            $storeIdList[] = $product->retailer->retailer_id;
        }
       
        $retailer = Retailer::find($storeIdList);
      

       
        return view('customer.find.reference', compact('plant_reference', 'retailer'));
    }

